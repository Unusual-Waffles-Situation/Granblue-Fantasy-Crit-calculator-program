class Weapon:
    def __init__(self, weaponName, critSkillType, weaponModType, weaponElement, weaponSkillLevel, weaponAmount):
        self.weaponName = weaponName
        self.critSkillType = critSkillType
        self.weaponModType = weaponModType
        self.weaponElement = weaponElement
        self.weaponSkillLevel = weaponSkillLevel
        self.weaponAmount = weaponAmount